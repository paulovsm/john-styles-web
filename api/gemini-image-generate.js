import { GoogleGenAI } from "@google/genai";

export default async function handler(req, res) {
    // CORS headers
    res.setHeader('Access-Control-Allow-Credentials', true);
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'GET,OPTIONS,PATCH,DELETE,POST,PUT');
    res.setHeader(
        'Access-Control-Allow-Headers',
        'X-CSRF-Token, X-Requested-With, Accept, Accept-Version, Content-Length, Content-MD5, Content-Type, Date, X-Api-Version'
    );

    if (req.method === 'OPTIONS') {
        res.status(200).end();
        return;
    }

    if (req.method !== 'POST') {
        return res.status(405).json({ error: 'Method not allowed' });
    }

    const { prompt, userImage, itemImages, itemImage } = req.body;

    if (!prompt) {
        return res.status(400).json({ error: 'Missing prompt' });
    }

    const apiKey = process.env.GOOGLE_AI_API_KEY;
    if (!apiKey) {
        return res.status(500).json({ error: 'API Key not configured' });
    }

    try {
        console.log("Generating image with Gemini for prompt:", prompt);

        const ai = new GoogleGenAI({ apiKey });

        // Helper to process base64 image data
        const processImage = (imgData) => {
            let base64Data = imgData;
            let mimeType = 'image/png';

            if (imgData.startsWith('data:')) {
                const matches = imgData.match(/^data:([^;]+);base64,(.+)$/);
                if (matches) {
                    mimeType = matches[1];
                    base64Data = matches[2];
                }
            }
            return {
                inlineData: {
                    mimeType: mimeType,
                    data: base64Data
                }
            };
        };

        // Prepare content parts
        let contents = [{ text: prompt }];

        if (userImage) {
            contents.push(processImage(userImage));
        }

        // Handle multiple item images
        if (itemImages && Array.isArray(itemImages)) {
            itemImages.forEach(img => {
                if (img) contents.push(processImage(img));
            });
        }
        // Backward compatibility or single item fallback
        else if (itemImage) {
            contents.push(processImage(itemImage));
        }

        const response = await ai.models.generateContent({
            model: "gemini-2.5-flash-image",
            //model: "gemini-3-pro-image-preview",
            contents: contents,
        });

        // Extract the image from the response
        let imageBase64 = null;

        for (const candidate of response.candidates) {
            for (const part of candidate.content.parts) {
                if (part.inlineData) {
                    imageBase64 = part.inlineData.data;
                    break;
                }
            }
            if (imageBase64) break;
        }

        if (!imageBase64) {
            console.error("No image data found in response");
            return res.status(500).json({ error: 'No image generated' });
        }

        // Return the image as a data URL
        const imageDataUrl = `data:image/png;base64,${imageBase64}`;

        return res.status(200).json({
            image: imageDataUrl,
            note: "Image generated by Gemini 2.5 Flash Image (Nano Banana)"
        });

    } catch (error) {
        console.error('Image Generation Error:', error);

        // Handle quota exceeded error (429)
        if (error.status === 429) {
            // Extract retry delay from error details if available
            let retryDelay = 60; // Default to 60 seconds
            if (error.errorDetails) {
                const retryInfo = error.errorDetails.find(detail =>
                    detail['@type'] === 'type.googleapis.com/google.rpc.RetryInfo'
                );
                if (retryInfo && retryInfo.retryDelay) {
                    const delayMatch = retryInfo.retryDelay.match(/(\d+)s?/);
                    if (delayMatch) {
                        retryDelay = parseInt(delayMatch[1]);
                    }
                }
            }

            return res.status(429).json({
                error: 'QUOTA_EXCEEDED',
                message: 'API quota exceeded. Please wait before trying again.',
                retryAfter: retryDelay,
                details: error.message
            });
        }

        // Handle other API errors
        return res.status(error.status || 500).json({
            error: 'GENERATION_FAILED',
            message: 'Failed to generate image',
            details: error.message
        });
    }
}
